# 存csv
def save_item_in_csv(item, file_name, titleNum=0):
    with open(file_name, "a", encoding="utf-8-sig") as f:
        writer = csv.DictWriter(f, fieldnames=item.keys())
        if titleNum == 0:
            writer.writeheader()
        writer.writerow(item)


